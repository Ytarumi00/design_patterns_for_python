
class GarageDoor:
  '''
  Vender class for GarageDoor
  '''
  
  def __init__(self):
    pass
  
  def up(self):
    print("open garage")
    
  def down(self):
    print("close garage")
    
  def stop(self):
    print("stop garage door")
    
  def lightOn(self):
    print("turn the light on")
    
  def lightOff(self):
    print("turn the light off")
    
    